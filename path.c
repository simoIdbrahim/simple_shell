#include "shell.h"

/**
 * is_cmd - Check if a given path points to a command.
 * @info: Pointer to the info structure.
 * @path: Path to check.
 *
 * Return: 1 if the path points to a command, 0 otherwise.
 */

int is_cmd(info_t *info, char *path)
{
	struct stat st;

	(void)info;
	if (!path || stat(path, &st))
		return (0);

	if (st.st_mode & S_IFREG)
	{
		return (1);
	}
	return (0);
}

/**
 * dup_chars - Duplicate characters from a string within a specified range.
 * @pathstr: Source string.
 * @start: Starting index.
 * @stop: Ending index.
 *
 * Return: Pointer to the duplicated characters.
 */

char *dup_chars(char *pathstr, int start, int stop)
{
	static char buf[1024];
	int i = 0, k = 0;

	for (k = 0, i = start; i < stop; i++)
		if (pathstr[i] != ':')
			buf[k++] = pathstr[i];
	buf[k] = 0;
	return (buf);
}

/**
 * find_path - Find the path of a command, using the PATH environment variable.
 * @info: Pointer to the info structure.
 * @pathstr: PATH environment variable value.
 * @cmd: Command name to find.
 *
 * Return: Full path of the command, or NULL if not found.
 */

char *find_path(info_t *info, char *pathstr, char *cmd)
{
	int i = 0, curr_pos = 0;
	char *path;

	if (!pathstr)
		return (NULL);

	if ((_strlen(cmd) > 2) && starts_with(cmd, "./"))
	{
		if (is_cmd(info, cmd))
			return (cmd);
	}

	while (1)
	{
		if (!pathstr[i] || pathstr[i] == ':')
		{
			path = dup_chars(pathstr, curr_pos, i);
			if (!*path)
				_strcat(path, cmd);
			else
			{
				_strcat(path, "/");
				_strcat(path, cmd);
			}
			if (is_cmd(info, path))
				return (path);
			if (!pathstr[i])
				break;
			curr_pos = i;
		}
		i++;
	}
	return (NULL);
}
